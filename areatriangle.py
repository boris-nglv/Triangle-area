import math

a = float (input("Въведи дължината на страната a: "))
b = float (input("Въведи дължината на страната b: "))
c = float (input("Въведи дължината на страната c: "))

#сборът на дължините на всяка от двете страни трябва да е по-голяма от третата
if (a + b > c, b + a > c, b + c > a):
    s = (a + b + c) / 2
    area = math.sqrt (s * (s - a) * (s - b) * (s -c) )

    print ("Лицето на триъгълника е", area)

else:
    print ("Неправилно въведени стойности за дължините")